package kr.willit.beans;

public class Hero {
	
	private String name;
	private String level;
	
	public Hero() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

}
